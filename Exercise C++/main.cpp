#include <iostream>
using namespace std;

int main()
{

    /* Hitung keliling persegi panjang */

    float panjang = 18;
    float lebar = 7;
    float keliling;

    keliling = 2 * (panjang + lebar);

    cout << "Panjang: " << panjang << endl;
    cout << "Lebar: " << lebar << endl;
    cout << "Keliling persegi panjang adalah: " << keliling << endl;
}
